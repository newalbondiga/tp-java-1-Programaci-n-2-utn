/*
 * Este programa demuestra la declaración de variables de diferentes tipos de datos
 * y la impresión de sus valores en la consola.
 */
package miprimerproyecto;

public class MiPrimerProyecto {

    public static void main(String[] args) {
        // a. Declarar una variable de tipo String para el nombre
        String nombre = "Juan Pérez";

        // b. Declarar una variable de tipo int para la edad
        int edad = 30;

        // c. Declarar una variable de tipo double para la altura
        double altura = 1.75;

        // d. Declarar una variable de tipo boolean para indicar si es estudiante
        boolean estudiante = true;

        // Imprimir los valores de las variables en la consola
        System.out.println("Nombre: " + nombre);
        System.out.println("Edad: " + edad + " años");
        System.out.println("Altura: " + altura + " metros");
        System.out.println("¿Es estudiante?: " + estudiante);
    }
}
